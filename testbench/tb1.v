`timescale 1ns/1ps

module tb1;

    //=========================
    // Testbench signals
    //=========================
    reg clk, rst;            // Clock and reset
    reg [3:0] sel;           // Operation select
    reg [7:0] in1, in2;      // Input operands

    // Outputs from ALU
    wire [7:0] Out1;
    wire [7:0] Out2;

    //=========================
    // Instantiate the ALU
    //=========================
    ALU t1 (
        .in1(in1),
        .in2(in2),
        .sel(sel),
        .clk(clk),
        .rst(rst),
        .out1(Out1),
        .out2(Out2)
    );

    //=========================
    // Clock generation
    //=========================
    initial begin

        // Create waveform file
        $dumpfile("tb1.vcd");

        // Dump all signals
        $dumpvars(0, tb1);

        // Initialize clock
        clk = 1'b0;

        // Generate 10 ns period clock
        forever
            #5 clk = ~clk;
    end

    //=========================
    // Apply test vectors
    //=========================
    initial begin

        // Reset sequence
        rst = 1'b0;
        #5  rst = 1'b1;
        #5  rst = 1'b0;
        #5  rst = 1'b1;
        #5  rst = 1'b0;

        //-------------------------
        // Test 1 : Addition
        //-------------------------
        #20;
        in1 = 8'b00100101;
        in2 = 8'b10110000;
        sel = 4'b0000;
        #20;

        //-------------------------
        // Test 2 : Subtraction
        //-------------------------
        in1 = 8'b10101101;
        in2 = 8'b11111010;
        sel = 4'b0001;
        #40;

        //-------------------------
        // Test 3 : Multiplication
        //-------------------------
        in1 = 8'b00000111;
        in2 = 8'b00000010;
        sel = 4'b0010;
        #20;

        //-------------------------
        // Test 4 : Division
        //-------------------------
        in1 = 8'b00001100;
        in2 = 8'b00000100;
        sel = 4'b0011;
        #40;

        //-------------------------
        // Test 5 : Modulus
        //-------------------------
        in1 = 8'b00000000;
        in2 = 8'b00000011;
        sel = 4'b0100;
        #20;

        //-------------------------
        // Test 6 : AND
        //-------------------------
        in1 = 8'b11001100;
        in2 = 8'b10101010;
        sel = 4'b0101;
        #20;

        //-------------------------
        // Test 7 : NAND
        //-------------------------
        in1 = 8'b11001100;
        in2 = 8'b10101010;
        sel = 4'b0110;
        #20;

        //-------------------------
        // Test 8 : OR
        //-------------------------
        in1 = 8'b11001100;
        in2 = 8'b10101010;
        sel = 4'b0111;
        #20;

        //-------------------------
        // Test 9 : NOR
        //-------------------------
        in1 = 8'b11001100;
        in2 = 8'b10101010;
        sel = 4'b1000;
        #20;

        //-------------------------
        // Test 10 : XOR
        //-------------------------
        in1 = 8'b11001100;
        in2 = 8'b10101010;
        sel = 4'b1001;
        #20;

        //-------------------------
        // Test 11 : XNOR
        //-------------------------
        in1 = 8'b11001100;
        in2 = 8'b10101010;
        sel = 4'b1010;
        #20;

        //-------------------------
        // Test 12 : Vector Reverse
        //-------------------------
        in1 = 8'b11001010;
        in2 = 8'b10100101;
        sel = 4'b1011;
        #20;

        //-------------------------
        // Test 13 : Parity Check
        //-------------------------
        in1 = 8'b11001010;
        sel = 4'b1100;
        #20;

        //-------------------------
        // Test 14 : Sign Detection
        //-------------------------
        in1 = 8'b10000000;
        sel = 4'b1101;
        #20;

        //-------------------------
        // Test 15 : Equality Check
        //-------------------------
        in1 = 8'b00000000;
        in2 = 8'b00000000;
        sel = 4'b1110;
        #20;

        //-------------------------
        // Test 16 : Average
        //-------------------------
        in1 = 8'b00001000;
        in2 = 8'b00000010;
        sel = 4'b1111;
        #20;

        // Wait before ending simulation
        #100;

        // Stop simulation
        $finish;

    end

endmodule
